package Project.src.main.java.com.testcode;

public class Coordinate 
{
    public int x = -1;
    public int y = -1;
   
    public Coordinate()
    {
        x = -1;
        y = -1;
    }

    public Coordinate(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public void set(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}
